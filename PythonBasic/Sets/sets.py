# Sets
"""Sets are unorderes collections of data items. They store multiple items in a single variable.
    Set items are separated by commas and enclosed within curly brackets{}.
    Sets are unchangeable, meaning you cannot chnage items of the set once created.
    Set do not contain duplicate items.
"""
info = {"Sumit", "Sumit", 12, 12, False, 5.8}
print(info)

# Accessing set items:
# using for loop
# You can access items of a set using a for loop
for item in info:
    print(item)


